package ch01;

public class _SwitchCase {
    public static void main(String[] args) {
        int ranking = 1;
        switch (ranking){
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("error!!!");
        }
    }
}
