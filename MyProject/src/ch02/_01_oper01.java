package ch02;

public class _01_oper01 {
    public static void main(String[] args) {
        int num = 10;

        num += 2;
        System.out.println(num);

        num -= 2;
        System.out.println(num);

        num *= 2;
        System.out.println(num);

        num /= 2;
        System.out.println(num);

        num %= 2;
        System.out.println(num);
    }
}
